public class Switch
{
	public static void main(String[] args) 
	{
		char c = 'a';
		switch (c)
		{
		case 'a':
			System.out.println("monday");
			break;
		case 'b':
			System.out.println("tuesday");
		case 'c':
			System.out.println("wednesday");
		default:
			System.out.println("unknown");
		}
	}
}
